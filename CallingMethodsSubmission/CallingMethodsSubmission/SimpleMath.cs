namespace CallingMethodsSubmission
{
    internal class SimpleMath
    {
        public int ToSecondPower(int number)
        {
            return number * number;
        }
        public int ToThirdPower(int number)
        {
            return number * number * number;
        }
        public int IncreaseByTen(int number)
        {
            return number + 10;
        }
    }
}
